    public void DoConnect(){
                
        try{
        String host="jdbc:derby://localhost:1527/Information";
        String uName="uttam";
        String uPass="uttam";
        con=DriverManager.getConnection(host, uName, uPass);
        stmt= con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, 
                                    ResultSet.CONCUR_UPDATABLE );
        
        String sql="SELECT * FROM STUDENT";
        rs=stmt.executeQuery(sql);
        
        rs.next();
        int sid_col=rs.getInt("sid");
        int age_col=rs.getInt("age");
        
        String id=Integer.toString(sid_col);
        String age=Integer.toString(age_col);
        String name_col=rs.getString("name");
        String add_col=rs.getString("address");
        
        
        
        jTextFieldSID.setText(id);
        jTextFieldName.setText(name_col);
        jTextFieldAge.setText(age);
        jTextFieldAddress.setText(add_col);
        
        //System.out.println(" "+name_col);
        
        }catch(SQLException s){
            System.out.println("SQL Exception: "+s);
        }
    }

