    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        try{
            rs.first();
            rs.previous();

            try{
                Integer.parseInt(jTextFieldSearch.getText());
            }catch(NumberFormatException n){
                JOptionPane.showMessageDialog(this, "Invalid input\n"
                    + "Student's Id must be an Integer value");
            }

            int searchID=Integer.parseInt(jTextFieldSearch.getText());
            int flag=0;
            while(rs.next()){
                if(searchID==rs.getInt("sid")){
                    flag=1;
                    String id=Integer.toString(rs.getInt("sid"));
                    String age=Integer.toString(rs.getInt("age"));
                    String name_col=rs.getString("name");
                    String add_col=rs.getString("address");

                    jTextFieldSID.setText(id);
                    jTextFieldName.setText(name_col);
                    jTextFieldAge.setText(age);
                    jTextFieldAddress.setText(add_col);

                    break;
                }

            }
            if(flag==0){
                JOptionPane.showMessageDialog(this, "No match found!!");
            }

        }catch(SQLException s){
            JOptionPane.showMessageDialog(this, s);
        }

    }//GEN-LAST:event_jButtonSearchActionPerformed

